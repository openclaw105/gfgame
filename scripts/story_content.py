# 格式：(speaker, line) 或 (speaker, line, bg) 或 (speaker, line, bg, {flags})
# flags: muscle_sprite, hot, xz_reveal 等
def _b(sp, line, bg=None, flags=None):
    if flags:
        if bg:
            return (sp, line, bg, flags)
        return (sp, line, flags)
    if bg:
        return (sp, line, bg)
    return (sp, line)


def _bm(sp, line, bg=None):
    return _b(sp, line, bg, {"muscle_sprite": True})


