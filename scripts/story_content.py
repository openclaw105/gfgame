def branch_for(ch_n, choice_i, opt_i, opt_text, speakers):
    """自然分支三句：行为 → 对方反应 → 收束"""
    custom = STORY.get(ch_n, {}).get("branch", {}).get(choice_i)
    if custom and opt_i < len(custom):
        return list(custom[opt_i])
    responders = [name for name, _ in speakers if name not in ("narrator", "葵")]
    sp = responders[(choice_i + opt_i) % len(responders)] if responders else "薇薇"
    line = opt_text if opt_text.endswith("。") else opt_text + "。"
    short = opt_text.strip("「」")
    if len(short) > 14:
        short = short[:14] + "……"
    reactions = [
        f"{sp}听完「{short}」，没有立刻反驳。",
        f"{sp}顺着「{short}」点头，把场面稳住。",
        f"{sp}把声音放轻：知道了，「{short}」我来配合。",
        f"{sp}看向旁边的人群，替「{short}」把话题挡了一下。",
        f"{sp}没有追问「{short}」，只把下一步交回现场。",
    ]
    closures = [
        f"你把「{short}」落成行动，眼前的局面终于往前挪了一步。",
        f"「{short}」没有立刻改变所有事，却让当下有了清晰的方向。",
        f"灯光和人声贴着耳边过去，你把「{short}」之后的话暂时收住。",
        f"手机屏幕暗下去，「{short}」这句决定被你记进心里。",
        f"周围安静了一点，你知道「{short}」之后就不能装作没想过。",
    ]
    k = (ch_n * 7 + choice_i * 3 + opt_i) % len(reactions)
    return [
        ("葵", line),
        (sp, reactions[k]),
        ("narrator", closures[(k + opt_i) % len(closures)]),
    ]
